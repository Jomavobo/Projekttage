import { Schema, model } from 'mongoose';

const teacherSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
});

// Export the Teacher model
export const Teacher = model('Teacher', teacherSchema);